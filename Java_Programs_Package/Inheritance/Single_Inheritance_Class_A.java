package Inheritance;

public class Single_Inheritance_Class_A {

    public void method_1()
    {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);
    }

    public void method_2()
    {
        int d = 30;
        int e = 20;
        int f = d - e;
        System.out.println(f);
    }
}
